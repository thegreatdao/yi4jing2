package iching.android.bean;

public class Divination
{
	private String question;
	private String originalLines;
	private String changingLines;
	
	public String getQuestion()
	{
		return question;
	}
	
	public void setQuestion(String question)
	{
		this.question = question;
	}
	
	public String getOriginalLines()
	{
		return originalLines;
	}
	
	public void setOriginalLines(String originalLines)
	{
		this.originalLines = originalLines;
	}
	
	public String getChangingLines()
	{
		return changingLines;
	}
	
	public void setChangingLines(String changingLines)
	{
		this.changingLines = changingLines;
	}
	
}
